#Hérna fæ ég þrjár tölurnar frá notandanum
tala_1=int(input("sláðu inn tölu 1 "))
tala_2=int(input("sláðu inn tölu 2 "))
tala_3=int(input("sláðu inn tölu 3 "))
#útreykningar
summa=tala_1+tala_2+tala_3#hér reykna ég summuna
margfeldi=tala_1*tala_2*tala_3#Hér margfalda ég tölurnar
fradrattur=tala_1-tala_2-tala_3#Hér dreg ég tölurnar frá
#Hérna sýni ég notandanum útkomuna
print("Summa talnana er:",summa)
print("Margfeldi talnana er:",margfeldi)
print("Frádráttur talnana er:",fradrattur)
